#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		length;
	int		i;
	char	*str;

	length = ft_strlen(s1);
	str = (char *)malloc((length + 1) * sizeof(char));
	if (!str)
		return (0);
	i = 0;
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
